package com.tcb.authGateway.sg;

public class SGXMLCoderException extends RuntimeException {

  public SGXMLCoderException() {
  }



  public SGXMLCoderException(String message) {
    super(message);
  }



  public SGXMLCoderException(String message, Throwable cause) {
    super(message, cause);
  }



  public SGXMLCoderException(Throwable cause) {
    super(cause);
  }
}